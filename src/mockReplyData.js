/**
 * 根据用户输入拼装一段较长的模拟回复。
 * @param {string} userText 用户提问
 * @returns {string} 最终用于流式渲染的长文本
 */
export function buildMockReply(userText) {
  const intro = `我先复述一下你的输入：「${userText}」。下面这段内容是专门为了“看起来像真实大模型流式回答”而写的长文本示例，你可以直接拿它做压测、交互演示和视觉联调。`

  return [intro, ...MOCK_REPLY_PARAGRAPHS].join('\n\n')
}
